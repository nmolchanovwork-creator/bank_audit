def normalize_text(text: str) -> str:
    """Нормалізація тексту: нижній регістр, очищення спецсимволів."""
    if pd.isna(text) or text is None:
        return ""
    text = str(text).lower().strip()
    # Замінюємо типові замінники апострофа
    text = re.sub(r"[`'''ʼ]", "'", text)
    # Нормалізуємо пробіли
    text = re.sub(r"\s+", " ", text)
    return text


def keyword_match(text: str, keywords: list) -> bool:
    """Перевірка наявності хоча б одного ключового слова в тексті."""
    norm_text = normalize_text(text)
    for kw in keywords:
        kw_norm = normalize_text(kw)
        if kw_norm in norm_text:
            return True
    return False


def find_matching_keywords(text: str, keywords: list) -> list:
    """Знаходить всі ключові слова, що зустрічаються в тексті."""
    norm_text = normalize_text(text)
    found = []
    for kw in keywords:
        if normalize_text(kw) in norm_text:
            found.append(kw)
    return found


def safe_float(value) -> float:
    """Безпечне перетворення у float."""
    try:
        if pd.isna(value):
            return 0.0
        cleaned = str(value).replace(" ", "").replace(
            ",", ".").replace("₴", "").strip()
        return float(cleaned) if cleaned else 0.0
    except (ValueError, TypeError):
        return 0.0


def find_column(df: pd.DataFrame, possible_names: list) -> str | None:
    """Шукає колонку за списком можливих назв (нечутливо до регістру)."""
    df_cols_lower = {c.lower().strip(): c for c in df.columns}
    for name in possible_names:
        if name.lower().strip() in df_cols_lower:
            return df_cols_lower[name.lower().strip()]
    return None


